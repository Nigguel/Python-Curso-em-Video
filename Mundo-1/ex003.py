"""
    Crie um programa que leia dois números e mostre a soma entre eles
"""

primer_valor = input("Digite um valor: ")
primer_valor_inteiro = int(primer_valor)
segundo_valor = input("Digite outro valor: ")
segundo_valor_inteiro = int(segundo_valor)

# Fazendo a soma dos valores

soma = primer_valor_inteiro + segundo_valor_inteiro

# mostrando Resultado

print(f"A soma entre {primer_valor_inteiro} e {segundo_valor_inteiro} é igual a {soma}")
